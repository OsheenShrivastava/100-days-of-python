
# Printing Strings
print("Hello World!")
print("\n")

# New Line "/n"
print("Hello World!\nHello World!")
print("\n")

# Concatinate Strings
print("Hello" + " World!")
print("Hello" + " " + "World!")
print("\n")

# Input Function - input() function will take an input from user
print("Hello " + input("What is your name? ") + "!")
print("\n")

# Variables
name = "Osheen"
print(name)
print("\n")

username = input("What is your name? ")
length = len(username)
print(length)
print("\n")

# Variable naming
username_of_this_person = "Osheen"
length = len(username_of_this_person)
print(length)